// این سرویس‌ورکر عمداً هیچ‌چیزی را کش نمی‌کند.
//
// چرا؟ چون سوغات‌شاپ قیمت لحظه‌ای ارز/کریپتو، موجودی محصولات و وضعیت سفارش‌ها
// را نشان می‌دهد؛ اگر این‌ها کش شوند، ممکن است کاربر قیمت یا اطلاعات قدیمی و
// نادرست ببیند. تنها هدف این فایل، برآورده کردن معیار «وجود یک سرویس‌ورکر
// فعال با fetch handler» است که برخی مرورگرها (خصوصاً اندروید/سامسونگ
// اینترنت) پیش از نمایش پرامپت «نصب اپلیکیشن» آن را بررسی می‌کنند.

self.addEventListener('install', () => {
  // بلافاصله جایگزین نسخه‌ی قبلی سرویس‌ورکر (در صورت وجود) می‌شود
  self.skipWaiting();
});

self.addEventListener('activate', (event) => {
  // کنترل تمام تب‌های باز را بدون نیاز به رفرش دستی به‌دست می‌گیرد
  event.waitUntil(self.clients.claim());
});

// پاس‌ترو خالص: هر درخواست مستقیماً به شبکه فرستاده می‌شود، بدون کش کردن
// هیچ پاسخی. این یعنی سایت همیشه آخرین و درست‌ترین نسخه‌ی محتوا را نشان
// می‌دهد، دقیقاً مثل حالتی که اصلاً سرویس‌ورکری وجود نداشت.
self.addEventListener('fetch', (event) => {
  event.respondWith(fetch(event.request));
});
