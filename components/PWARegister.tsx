'use client';

import { useEffect } from 'react';

/**
 * ثبت سرویس‌ورکر — به‌صورت سراسری در app/[locale]/layout.tsx قرار می‌گیرد
 * (نه فقط صفحه‌ی اصلی)، چون داشتنِ یک سرویس‌ورکر فعال با fetch handler یکی
 * از پیش‌نیازهای برخی مرورگرها (خصوصاً اندروید/سامسونگ اینترنت) برای «قابل‌
 * نصب» دانستن سایت و شلیک کردن ایونت beforeinstallprompt است.
 *
 * خودِ سرویس‌ورکر (public/sw.js) عمداً هیچ‌چیزی کش نمی‌کند — چون این سایت
 * قیمت لحظه‌ای ارز/کریپتو و موجودی محصولات دارد و کش کردن صفحات می‌تواند
 * اطلاعات قدیمی/نادرست به کاربر نشان دهد. این کامپوننت فقط رندری ندارد و
 * صرفاً کار ثبت سرویس‌ورکر را انجام می‌دهد.
 */
export default function PWARegister() {
  useEffect(() => {
    if (typeof window === 'undefined' || !('serviceWorker' in navigator)) return;

    // در حالت توسعه (next dev) عمداً ثبت نمی‌کنیم تا سرویس‌ورکرِ کش‌شده در
    // مرورگر باعث دردسر و رفتار غیرمنتظره در توسعه‌ی محلی نشود.
    if (process.env.NODE_ENV !== 'production') return;

    navigator.serviceWorker.register('/sw.js').catch(() => {
      // اگر ثبت با خطا مواجه شد بی‌صدا نادیده می‌گیریم؛ کل سایت باید بدون
      // مشکل و کامل، حتی بدون سرویس‌ورکر هم کار کند.
    });
  }, []);

  return null;
}
